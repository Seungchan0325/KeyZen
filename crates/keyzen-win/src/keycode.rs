use keyzen_core::Key;
use windows::Win32::UI::Input::KeyboardAndMouse::*;

pub fn key_to_vk(key: Key) -> VIRTUAL_KEY {
    match key {
        Key::A => VK_A,
        Key::B => VK_B,
        Key::C => VK_C,
        Key::D => VK_D,
        Key::E => VK_E,
        Key::F => VK_F,
        Key::G => VK_G,
        Key::H => VK_H,
        Key::I => VK_I,
        Key::J => VK_J,
        Key::K => VK_K,
        Key::L => VK_L,
        Key::M => VK_M,
        Key::N => VK_N,
        Key::O => VK_O,
        Key::P => VK_P,
        Key::Q => VK_Q,
        Key::R => VK_R,
        Key::S => VK_S,
        Key::T => VK_T,
        Key::U => VK_U,
        Key::V => VK_V,
        Key::W => VK_W,
        Key::X => VK_X,
        Key::Y => VK_Y,
        Key::Z => VK_Z,
        Key::Num0 => VK_0,
        Key::Num1 => VK_1,
        Key::Num2 => VK_2,
        Key::Num3 => VK_3,
        Key::Num4 => VK_4,
        Key::Num5 => VK_5,
        Key::Num6 => VK_6,
        Key::Num7 => VK_7,
        Key::Num8 => VK_8,
        Key::Num9 => VK_9,
        Key::Escape => VK_ESCAPE,
        Key::Tab => VK_TAB,
        Key::CapsLock => VK_CAPITAL,
        Key::Space => VK_SPACE,
        Key::Enter => VK_RETURN,
        Key::Backspace => VK_BACK,
        Key::Left => VK_LEFT,
        Key::Right => VK_RIGHT,
        Key::Up => VK_UP,
        Key::Down => VK_DOWN,
        Key::Home => VK_HOME,
        Key::End => VK_END,
        Key::PageUp => VK_PRIOR,
        Key::PageDown => VK_NEXT,
        Key::Insert => VK_INSERT,
        Key::Delete => VK_DELETE,
        Key::Minus => VK_OEM_MINUS,
        Key::Equal => VK_OEM_PLUS,
        Key::LeftBracket => VK_OEM_4,
        Key::RightBracket => VK_OEM_6,
        Key::Backslash => VK_OEM_5,
        Key::Semicolon => VK_OEM_1,
        Key::Quote => VK_OEM_7,
        Key::Grave => VK_OEM_3,
        Key::Comma => VK_OEM_COMMA,
        Key::Dot => VK_OEM_PERIOD,
        Key::Slash => VK_OEM_2,
        Key::Function(n) => VIRTUAL_KEY(VK_F1.0 + u16::from(n.saturating_sub(1))),
        Key::LeftCtrl => VK_LCONTROL,
        Key::RightCtrl => VK_RCONTROL,
        Key::LeftAlt => VK_LMENU,
        Key::RightAlt => VK_RMENU,
        Key::LeftShift => VK_LSHIFT,
        Key::RightShift => VK_RSHIFT,
        Key::LeftMeta => VK_LWIN,
        Key::RightMeta => VK_RWIN,
    }
}

pub fn vk_to_key(vk: u32) -> Option<Key> {
    Some(match VIRTUAL_KEY(vk as u16) {
        VK_A => Key::A,
        VK_B => Key::B,
        VK_C => Key::C,
        VK_D => Key::D,
        VK_E => Key::E,
        VK_F => Key::F,
        VK_G => Key::G,
        VK_H => Key::H,
        VK_I => Key::I,
        VK_J => Key::J,
        VK_K => Key::K,
        VK_L => Key::L,
        VK_M => Key::M,
        VK_N => Key::N,
        VK_O => Key::O,
        VK_P => Key::P,
        VK_Q => Key::Q,
        VK_R => Key::R,
        VK_S => Key::S,
        VK_T => Key::T,
        VK_U => Key::U,
        VK_V => Key::V,
        VK_W => Key::W,
        VK_X => Key::X,
        VK_Y => Key::Y,
        VK_Z => Key::Z,
        VK_0 => Key::Num0,
        VK_1 => Key::Num1,
        VK_2 => Key::Num2,
        VK_3 => Key::Num3,
        VK_4 => Key::Num4,
        VK_5 => Key::Num5,
        VK_6 => Key::Num6,
        VK_7 => Key::Num7,
        VK_8 => Key::Num8,
        VK_9 => Key::Num9,
        VK_ESCAPE => Key::Escape,
        VK_TAB => Key::Tab,
        VK_CAPITAL => Key::CapsLock,
        VK_SPACE => Key::Space,
        VK_RETURN => Key::Enter,
        VK_BACK => Key::Backspace,
        VK_LEFT => Key::Left,
        VK_RIGHT => Key::Right,
        VK_UP => Key::Up,
        VK_DOWN => Key::Down,
        VK_HOME => Key::Home,
        VK_END => Key::End,
        VK_PRIOR => Key::PageUp,
        VK_NEXT => Key::PageDown,
        VK_INSERT => Key::Insert,
        VK_DELETE => Key::Delete,
        VK_OEM_MINUS => Key::Minus,
        VK_OEM_PLUS => Key::Equal,
        VK_OEM_4 => Key::LeftBracket,
        VK_OEM_6 => Key::RightBracket,
        VK_OEM_5 => Key::Backslash,
        VK_OEM_1 => Key::Semicolon,
        VK_OEM_7 => Key::Quote,
        VK_OEM_3 => Key::Grave,
        VK_OEM_COMMA => Key::Comma,
        VK_OEM_PERIOD => Key::Dot,
        VK_OEM_2 => Key::Slash,
        VK_LCONTROL => Key::LeftCtrl,
        VK_RCONTROL => Key::RightCtrl,
        VK_LMENU => Key::LeftAlt,
        VK_RMENU => Key::RightAlt,
        VK_LSHIFT => Key::LeftShift,
        VK_RSHIFT => Key::RightShift,
        VK_LWIN => Key::LeftMeta,
        VK_RWIN => Key::RightMeta,
        key if key.0 >= VK_F1.0 && key.0 <= VK_F24.0 => Key::Function((key.0 - VK_F1.0 + 1) as u8),
        _ => return None,
    })
}
